           <h2> <div class="sap"> DESCRIPTION </div></h2>
           Situé au cœur du bassin arachidier du Sénégal dans la région de Diourbel et à 120 km de Dakar, l’Institut Supérieur de Formation Agricole et Rurale (ISFAR) de Bambey résulte de l’intégration de l’École Nationale des Cadres ruraux de Bambey en 2005 dans le pôle agronomique de l’Université de Thiès qu’il partage avec l’Unité de Formation et de Recherche Sciences Agronomiques et Développement Rural (UFR SADR) ex ENSA.

           L’érection de l’ISFAR en institut d’université dans le pôle agronomique de l’Université de Thiès (UT) répond à la volonté du Ministère de mettre en synergie la formation des ingénieurs du monde rural en vue de la pérenniser dans un cadre apaisé propice à son développement et sa montée en puissance.

           Il en résulte la constitution d’un grand pôle de formation et de recherche agricoles et agroalimentaires à même de participer au développement de l’agriculture de notre pays et de l’Afrique.
      
